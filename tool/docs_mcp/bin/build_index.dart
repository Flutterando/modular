// Build-time indexer: walks the flutter_modular docs and emits a self-contained
// Dart source (`lib/src/generated/docs_data.g.dart`) embedding every page and
// its heading-delimited chunks, so the MCP server needs no filesystem at run
// time.
//
// Run from the package root:
//   dart run bin/build_index.dart [docsRoot]
//
// `docsRoot` defaults to the repo's `doc/docs` (resolved relative to this file).

import 'dart:convert';
import 'dart:io';

void main(List<String> args) {
  final docsRoot = args.isNotEmpty
      ? Directory(args.first)
      : Directory.fromUri(Platform.script.resolve('../../../doc/docs/'));

  if (!docsRoot.existsSync()) {
    stderr.writeln('Docs root not found: ${docsRoot.path}');
    exitCode = 1;
    return;
  }

  final pages = <_Page>[];
  final files =
      docsRoot
          .listSync(recursive: true)
          .whereType<File>()
          .where((f) => f.path.endsWith('.md'))
          .toList()
        ..sort((a, b) => a.path.compareTo(b.path));

  for (final file in files) {
    final rel = _relativePosix(docsRoot, file);
    if (!_include(rel)) continue;
    pages.add(_parsePage(rel, file.readAsStringSync()));
  }

  final out = File.fromUri(
    Platform.script.resolve('../lib/src/generated/docs_data.g.dart'),
  );
  out.parent.createSync(recursive: true);
  out.writeAsStringSync(_emit(pages));

  final chunkCount = pages.fold<int>(0, (a, p) => a + p.chunks.length);
  stdout.writeln(
    'Indexed ${pages.length} pages, $chunkCount chunks -> ${out.path}',
  );
}

/// Scope: top-level docs (intro/platforms) + flutter_modular v7, excluding the
/// legacy pages (`legacy/`, `legacy-6/`, …) and the deprecated shelf_modular
/// section.
bool _include(String rel) {
  if (RegExp(r'(^|/)legacy').hasMatch(rel) ||
      rel.contains('shelf_modular/')) {
    return false;
  }
  return rel.startsWith('flutter_modular/') || !rel.contains('/');
}

String _relativePosix(Directory root, File file) {
  var rel = file.path.substring(root.path.length);
  rel = rel.replaceAll(r'\', '/');
  if (rel.startsWith('/')) rel = rel.substring(1);
  return rel;
}

_Page _parsePage(String rel, String raw) {
  final body = _stripFrontMatter(raw).trim();

  final titleMatch = RegExp(r'^#\s+(.+)$', multiLine: true).firstMatch(body);
  final title = titleMatch?.group(1)?.trim() ?? _titleFromPath(rel);

  final chunks = <_Chunk>[];
  final buffer = StringBuffer();
  var heading = '';

  void flush() {
    final text = buffer.toString().trim();
    if (text.isNotEmpty) chunks.add(_Chunk(heading, text));
    buffer.clear();
  }

  final headingRe = RegExp(r'^(#{2,3})\s+(.+)$');
  for (final line in body.split('\n')) {
    final m = headingRe.firstMatch(line);
    if (m != null) {
      flush();
      heading = m.group(2)!.trim();
    }
    buffer.writeln(line);
  }
  flush();

  return _Page(path: rel, title: title, markdown: body, chunks: chunks);
}

String _stripFrontMatter(String raw) {
  if (!raw.startsWith('---')) return raw;
  final end = raw.indexOf('\n---', 3);
  if (end < 0) return raw;
  final newline = raw.indexOf('\n', end + 1);
  return newline < 0 ? '' : raw.substring(newline + 1);
}

String _titleFromPath(String rel) {
  final name = rel.split('/').last.replaceAll('.md', '');
  return name.isEmpty
      ? rel
      : name[0].toUpperCase() + name.substring(1).replaceAll('-', ' ');
}

String _slug(String heading) => heading
    .toLowerCase()
    .replaceAll(RegExp(r'[^a-z0-9]+'), '-')
    .replaceAll(RegExp(r'^-+|-+$'), '');

String _emit(List<_Page> pages) {
  String s(String value) => jsonEncode(value).replaceAll(r'$', r'\$');

  final b = StringBuffer()
    ..writeln('// GENERATED by bin/build_index.dart — do not edit by hand.')
    ..writeln('// Regenerate with: dart run bin/build_index.dart')
    ..writeln('// ignore_for_file: type=lint')
    ..writeln()
    ..writeln("import '../doc_chunk.dart';")
    ..writeln()
    ..writeln('const List<DocPage> docPages = [');
  for (final page in pages) {
    b
      ..writeln('  DocPage(')
      ..writeln('    path: ${s(page.path)},')
      ..writeln('    title: ${s(page.title)},')
      ..writeln('    markdown: ${s(page.markdown)},')
      ..writeln('  ),');
  }
  b
    ..writeln('];')
    ..writeln()
    ..writeln('const List<DocChunk> docChunks = [');
  for (final page in pages) {
    for (final chunk in page.chunks) {
      b
        ..writeln('  DocChunk(')
        ..writeln('    pagePath: ${s(page.path)},')
        ..writeln('    pageTitle: ${s(page.title)},')
        ..writeln('    heading: ${s(chunk.heading)},')
        ..writeln('    anchor: ${s(_slug(chunk.heading))},')
        ..writeln('    text: ${s(chunk.text)},')
        ..writeln('  ),');
    }
  }
  b.writeln('];');
  return b.toString();
}

class _Page {
  _Page({
    required this.path,
    required this.title,
    required this.markdown,
    required this.chunks,
  });
  final String path;
  final String title;
  final String markdown;
  final List<_Chunk> chunks;
}

class _Chunk {
  _Chunk(this.heading, this.text);
  final String heading;
  final String text;
}
